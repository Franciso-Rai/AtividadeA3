    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        int id_vendedor = Integer.parseInt(txtIdVendedor.getText());
        String nome = txtNome.getText();
        String unidade = txtUnidade.getText();
        try{
            Vendedor vendedor = new Vendedor (id_vendedor, nome, unidade);
            DAO dao = new DAO();
            dao.atualizarVendedor(vendedor);
            JOptionPane.showMessageDialog(null, "Vendedor atualizado!");
} catch(Exception e){
    JOptionPane.showMessageDialog(null,"Erro ao atualizar!");
}
    }//GEN-LAST:event_btnAtualizarActionPerformed

